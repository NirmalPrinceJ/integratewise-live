/**
 * Section 4 — LOADER: 8-Stage Universal Ingestion Pipeline
 *
 * Real implementation of all 8 stages:
 *   S1 Analyzer   — SHA-256 fingerprint, envelope with metadata
 *   S2 Classifier — data_kind detection, domain mapping, PII sensitivity scan
 *   S3 Filter     — Idempotency check (D1 fingerprints), tenant validation
 *   S4 Refiner    — Split composites, extract cross-ref IDs, normalize field names
 *   S5 Extractor  — Map to canonical schema, extract text/file metadata
 *   S6 Validator  — Required field checks, type validation, warning vs error
 *   S7 Split Router — Generate write plan (Spine / Context / Audit targets)
 *   S8 Writers    — Execute write plan, enqueue async analysis
 */

export interface IngestionContext {
  tenant_id: string;
  source: string;
  requestId: string;
  category?: string;
  user_id?: string;
  account_id?: string;
  team_id?: string;
  user_role?: string;
  /** Cloudflare D1 binding for fingerprint dedup */
  db?: D1Database;
  /** Spine v2 store URL */
  storeUrl?: string;
  /** THINK_QUEUE for async analysis */
  thinkQueue?: Queue;
}

/** Enriched envelope that accumulates metadata through stages */
export interface PipelineEnvelope {
  /** Original raw data */
  raw: any;
  /** SHA-256 fingerprint of the raw payload */
  fingerprint: string;
  /** Metadata accumulated by stages */
  meta: {
    source: string;
    tenant_id: string;
    request_id: string;
    received_at: string;
    data_kind: 'entity' | 'activity' | 'event' | 'file' | 'unknown';
    domain: string;
    sensitivity: 'public' | 'internal' | 'confidential' | 'restricted';
    pii_fields: string[];
    entity_type: string;
    is_duplicate: boolean;
    validation_errors: string[];
    validation_warnings: string[];
  };
  /** Canonical extracted fields */
  canonical: Record<string, unknown>;
  /** Write plan generated by S7 */
  write_plan: {
    spine: boolean;
    context_store: boolean;
    audit: boolean;
    targets: string[];
  };
  /** Sub-entities (from composite splits in S4) */
  children: any[];
}

// ─── Helpers ──────────────────────────────────────────────────────────────

async function sha256(data: string): Promise<string> {
  const encoder = new TextEncoder();
  const buf = await crypto.subtle.digest('SHA-256', encoder.encode(data));
  return Array.from(new Uint8Array(buf)).map(b => b.toString(16).padStart(2, '0')).join('');
}

/** Deterministic JSON serialization for fingerprinting */
function stableStringify(obj: any): string {
  if (obj === null || obj === undefined) return '';
  if (typeof obj !== 'object') return String(obj);
  if (Array.isArray(obj)) return `[${obj.map(stableStringify).join(',')}]`;
  return `{${Object.keys(obj).sort().map(k => `${k}:${stableStringify(obj[k])}`).join(',')}}`;
}

